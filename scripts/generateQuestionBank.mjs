function buildCategory(categoryId, prefix) {
  const categoryFacts = facts[categoryId];
  const answers = [...new Set(categoryFacts.map(([, answer]) => answer))];

  return categoryFacts.map(([clue, answer], i) => {
    const questionText = `${clue}は何でしょう？`;
    const choices = makeChoices(answer, answers, i);

    return {
      id: `${prefix}-${String(i + 1).padStart(4, "0")}`,
      question: questionText,
      answer,
      reading: readings.get(answer) ?? answer,
      acceptableAnswers: buildAcceptableAnswers(answer),
      categoryId,
      difficulty: difficultyFor(i, categoryFacts.length),
      choices,
      tags: ["curated-baseline"],
      ...(categoryId === "news" ? { publishedAt: publishedAtFor(i) } : {}),
    };
  });
}

function makeChoices(answer, allAnswers, seed) {
  const pool = allAnswers.filter((item) => item !== answer);
  const choices = [answer];
  let cursor = seed * 7 + 3;

  while (choices.length < 4) {
    const candidate = pool[cursor % pool.length];
    if (!choices.includes(candidate)) {
      choices.push(candidate);
    }
    cursor += 5;
  }

  return rotate(choices, seed);
}

function rotate(items, seed) {
  const offset = seed % items.length;
  return [...items.slice(offset), ...items.slice(0, offset)];
}

function difficultyFor(index, target) {
  const ratio = index / target;
  if (ratio < 0.45) return 1;
  if (ratio < 0.8) return 2;
  return 3;
}

function buildAcceptableAnswers(answer) {
  const answers = new Set();
  if (answer.endsWith("年")) answers.add(answer.replace("年", ""));
  if (answer.includes("・")) answers.add(answer.replaceAll("・", ""));
  if (/^[A-Z0-9.]+$/.test(answer)) answers.add(answer.toLowerCase());
  return [...answers];
}

function publishedAtFor(index) {
  const month = 4 + (index % 3);
  const day = 1 + (index % 28);
  return `2026-${String(month).padStart(2, "0")}-${String(day).padStart(2, "0")}`;
}
